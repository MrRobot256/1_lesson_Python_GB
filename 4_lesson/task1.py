def get_salary(hours, rate, prize):
    """
    Функция рассчитывает заработную плату сотрудника и принимает в себя следующие значения:
    hours - часы сотрудника
    rate - ставка в час сотрудника
    prize - премия сотрудника
    функция возвращет значение результат расчетов
    """
    result = (hours * rate) + prize
    return print(result)


